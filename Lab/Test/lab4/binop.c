  int a[2];
  int x = 2;
  int y = 1;
  a[0] = 1;

  write(x + 1);
  write(x + y);

  write(x - 1);
  write(x - y);

  write(x * 1);
  write(x * y);

  write(x / 1);
  write(x / y);

